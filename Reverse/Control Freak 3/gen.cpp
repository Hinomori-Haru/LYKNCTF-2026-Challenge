static uint32_t fold_poison(uint32_t poison)
{
    return (poison | (poison >> 8) | (poison >> 16) | (poison >> 24)) & 0xffu;
}

static uint32_t byte_mix(uint32_t x, uint32_t idx, uint32_t seed)
{
    x &= 0xffu;
    idx &= 0xffu;
    uint32_t v = (x | (idx << 8)) ^ (seed + 0x9e3779b9u);
    v = rotl32(v * 0x45d9f3bu + (x << ((idx & 3u) + 1u)), ((seed >> 27) & 15u) + 5u);
    v ^= v >> 11;
    v *= 0x27d4eb2du;
    v ^= rotl32(x * 0x165667b1u + idx + seed, ((x ^ seed) & 7u) + 3u);
    return v;
}

static uint32_t pair_mix(uint32_t a, uint32_t b, uint32_t i, uint32_t j, uint32_t seed)
{
    a &= 0xffu;
    b &= 0xffu;
    i &= 0xffu;
    j &= 0xffu;
    uint32_t v = seed ^ (i * 0x9e3779b1u) ^ (j * 0x85ebca77u);
    v += (a + 0x101u) * (seed | 1u);
    v = rotl32(v ^ ((b + 0x51u) * 0xc2b2ae3du), ((seed ^ (i << 1) ^ j) & 15u) + 5u);
    v += ((a ^ b) + 0x33u) * 0x27d4eb2du;
    v ^= rotl32((a + (b << 8) + i * 17u + j * 29u) * 0x165667b1u, ((seed >> 5) & 15u) + 3u);
    v += (a * b + 0x9e37u) * ((seed >> 16) | 1u);
    v ^= v >> 13;
    v *= 0x85ebca6bu;
    v ^= v >> 16;
    return v;
}

static uint32_t tri_mix(uint32_t a, uint32_t b, uint32_t c, uint32_t i, uint32_t j, uint32_t k, uint32_t seed)
{
    a &= 0xffu;
    b &= 0xffu;
    c &= 0xffu;
    i &= 0xffu;
    j &= 0xffu;
    k &= 0xffu;
    uint32_t v = seed + (i + 1u) * 0x7f4a7c15u + (j + 3u) * 0x94d049bbu + (k + 5u) * 0xd6e8feb9u;
    v ^= (a + 0x21u) * (b + 0x35u) * 0x45d9f3bu;
    v = rotl32(v, ((seed >> 11) & 15u) + 7u);
    v += ((b ^ c) + 0x123u) * 0x27d4eb2du;
    v ^= rotl32((a << 16) ^ (b << 8) ^ c ^ seed, ((i + j + k) & 15u) + 3u);
    v += (a * c + b * 0x101u + 0xbeefu) * 0x165667b1u;
    v ^= v >> 15;
    v *= 0xc2b2ae35u;
    v ^= v >> 16;
    return v;
}

static uint64_t global_mix(const unsigned char *input, std::size_t len, uint32_t seed)
{
    uint64_t s = 0x243f6a8885a308d3ull ^ ((uint64_t)len * 0x9e3779b97f4a7c15ull) ^ seed;
    uint64_t t = 0x13198a2e03707344ull ^ rotl64((uint64_t)seed | 1ull, 29);

    for (uint32_t pass = 0; pass < 3; ++pass) {
        for (uint32_t i = 0; i < 64; ++i) {
            const uint32_t ch = i < len ? input[i] : ((0xa7u ^ i * 31u ^ pass * 17u ^ (uint32_t)len) & 0xffu);
            const uint64_t lane = (uint64_t)ch | ((uint64_t)(i + pass * 67u) << 8) | (((uint64_t)len & 0xffull) << 24);
            s = rotl64(s ^ lane ^ t, ((i + pass) & 31u) + 5u) * 0x100000001b3ull + ch + seed + pass;
            t ^= rotl64(s + ((uint64_t)(ch + 0x101u) * 0xd6e8feb86659fd93ull), ((ch ^ i ^ seed) & 31u) + 1u);
            t = t * 0x94d049bb133111ebull + i + pass;
        }
        s ^= rotl64(t, 17u + pass * 9u);
    }

    return s ^ rotl64(t, 23) ^ ((uint64_t)len * 0xbf58476d1ce4e5b9ull);
}

enum VmOp : uint8_t {
    OpHalt = 0,
    OpEnd = 1,
    OpLen = 2,
    OpByte = 3,
    OpPair = 4,
    OpTri = 5,
    OpGlobal = 6,
    OpNoise = 7
};

struct VmState {
    const unsigned char *input;
    std::size_t len;
    uint64_t guard;
    uint64_t acc;
    uint64_t scratch;
    bool fault;
};

static uint8_t decode_opcode(uint8_t raw)
{
    return (uint8_t)(kOpDecodeEnc[raw] ^ ((raw * 37u + 0x5au) & 0xffu));
}

static bool read_u8(const unsigned char *block, std::size_t size, std::size_t *ip, uint8_t *out)
{
    if (*ip >= size) {
        return false;
    }
    *out = block[(*ip)++];
    return true;
}

static bool read_u32(const unsigned char *block, std::size_t size, std::size_t *ip, uint32_t *out)
{
    if (*ip + 4 > size) {
        return false;
    }
    uint32_t v = 0;
    std::memcpy(&v, block + *ip, sizeof(v));
    *ip += 4;
    *out = v;
    return true;
}

static bool read_u64(const unsigned char *block, std::size_t size, std::size_t *ip, uint64_t *out)
{
    if (*ip + 8 > size) {
        return false;
    }
    uint64_t v = 0;
    std::memcpy(&v, block + *ip, sizeof(v));
    *ip += 8;
    *out = v;
    return true;
}

static uint32_t vm_byte(const VmState &vm, uint32_t idx)
{
    return idx < vm.len ? vm.input[idx] : ((0xa5u ^ idx * 17u ^ (uint32_t)vm.len) & 0xffu);
}

static bool run_block(VmState *vm, uint32_t block_id, bool *halted)
{
    const BlockMeta &meta = kBlockMeta[block_id];
    unsigned char block[224];
    if (meta.size > sizeof(block) || (std::size_t)meta.offset + meta.size > sizeof(kVmBlob)) {
        vm->fault = true;
        return false;
    }

    uint64_t stream = meta.key ^ rotl64(vm->guard + meta.salt + (uint64_t)(block_id + 1u) * 0xd6e8feb86659fd93ull,
                                        ((block_id * 11u) & 63u) + 1u);
    for (uint32_t i = 0; i < meta.size; ++i) {
        const uint8_t mask = (uint8_t)(stream_byte(&stream, i) ^
                            ((i * 29u + block_id * 17u + ((vm->guard >> ((i & 7u) * 8u)) & 0xffu)) & 0xffu));
        block[i] = (uint8_t)(kVmBlob[meta.offset + i] ^ mask);
    }

    std::size_t ip = 0;
    bool done = false;
    while (!done && ip < meta.size) {
        uint8_t raw = 0;
        if (!read_u8(block, meta.size, &ip, &raw)) {
            vm->fault = true;
            break;
        }

        const uint8_t op = decode_opcode(raw);
        switch (op) {
        case OpHalt:
            *halted = true;
            done = true;
            break;

        case OpEnd:
            done = true;
            break;

        case OpLen: {
            uint32_t target = 0, seed = 0;
            if (!read_u32(block, meta.size, &ip, &target) || !read_u32(block, meta.size, &ip, &seed)) {
                vm->fault = true;
                break;
            }
            const uint32_t n = (uint32_t)vm->len;
            const uint32_t v = rotl32(((n ^ seed) * 0x45d9f3bu) + (n << 8) + 0x9e3779b9u,
                                      ((seed >> 27) & 7u) + 5u) ^ (seed >> 3);
            vm->acc |= (uint64_t)(v ^ target);
            break;
        }

        case OpByte: {
            uint8_t idx = 0;
            uint32_t target = 0, seed = 0;
            if (!read_u8(block, meta.size, &ip, &idx) ||
                !read_u32(block, meta.size, &ip, &target) ||
                !read_u32(block, meta.size, &ip, &seed)) {
                vm->fault = true;
                break;
            }
            vm->acc |= (uint64_t)(byte_mix(vm_byte(*vm, idx), idx, seed) ^ target);
            break;
        }

        case OpPair: {
            uint8_t i = 0, j = 0;
            uint32_t target = 0, seed = 0;
            if (!read_u8(block, meta.size, &ip, &i) ||
                !read_u8(block, meta.size, &ip, &j) ||
                !read_u32(block, meta.size, &ip, &target) ||
                !read_u32(block, meta.size, &ip, &seed)) {
                vm->fault = true;
                break;
            }
            vm->acc |= (uint64_t)(pair_mix(vm_byte(*vm, i), vm_byte(*vm, j), i, j, seed) ^ target);
            break;
        }

        case OpTri: {
            uint8_t i = 0, j = 0, k = 0;
            uint32_t target = 0, seed = 0;
            if (!read_u8(block, meta.size, &ip, &i) ||
                !read_u8(block, meta.size, &ip, &j) ||
                !read_u8(block, meta.size, &ip, &k) ||
                !read_u32(block, meta.size, &ip, &target) ||
                !read_u32(block, meta.size, &ip, &seed)) {
                vm->fault = true;
                break;
            }
            vm->acc |= (uint64_t)(tri_mix(vm_byte(*vm, i), vm_byte(*vm, j), vm_byte(*vm, k), i, j, k, seed) ^ target);
            break;
        }

        case OpGlobal: {
            uint64_t target = 0;
            uint32_t seed = 0;
            if (!read_u64(block, meta.size, &ip, &target) || !read_u32(block, meta.size, &ip, &seed)) {
                vm->fault = true;
                break;
            }
            vm->acc |= global_mix(vm->input, vm->len, seed) ^ target;
            break;
        }

        case OpNoise: {
            uint32_t salt = 0;
            if (!read_u32(block, meta.size, &ip, &salt)) {
                vm->fault = true;
                break;
            }
            vm->scratch ^= rotl64(vm->scratch + salt + vm->acc, ((salt >> 27) & 31u) + 1u);
            if (opaque_false(salt)) {
                vm->acc |= vm->scratch;
            }
            break;
        }

        default:
            vm->fault = true;
            done = true;
            break;
        }
    }

    std::memset(block, 0, sizeof(block));
    return !vm->fault;
}

static bool guarded_check(const char *input)
{
    const std::size_t len = bounded_len(input);
    const uint32_t poison = anti_probe();
    VmState vm;
    vm.input = (const unsigned char *)input;
    vm.len = len;
    vm.guard = kBaseGuard;
    vm.acc = fold_poison(poison);
    vm.scratch = 0x6a09e667f3bcc909ull ^ ((uint64_t)len << 32) ^ poison;
    vm.fault = false;

    bool halted = false;
    for (uint32_t block_id = 0; block_id < sizeof(kBlockMeta) / sizeof(kBlockMeta[0]); ++block_id) {
        if (!run_block(&vm, block_id, &halted)) {
            return false;
        }
        vm.guard = rotl64(vm.guard ^ kBlockMeta[block_id].mac ^
                          ((uint64_t)(block_id + 1u) * 0x9e3779b97f4a7c15ull),
                          ((block_id + 3u) * 7u) & 63u);
        if (halted) {
            break;
        }
    }

    return halted && !vm.fault && vm.acc == 0;
}

