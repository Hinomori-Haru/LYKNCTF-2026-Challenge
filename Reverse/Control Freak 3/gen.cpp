static uint32_t rotl32(uint32_t x, unsigned int r)
{
    r &= 31;
    return r == 0 ? x : (uint32_t)((x << r) | (x >> (32 - r)));
}

static uint64_t rotl64(uint64_t x, unsigned int r)
{
    r &= 63;
    return r == 0 ? x : (uint64_t)((x << r) | (x >> (64 - r)));
}

